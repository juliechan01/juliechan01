function over(element) {
    element.style.backgroundColor = "skyblue";
}

function out(element) {
    element.style.backgroundColor = "royalblue";
}

function emailButton() {
    document.getElementById("email").style.backgroundColor = "rgb(246,236,190)";
}

function emailOut() {
    document.getElementById("email").style.backgroundColor = "goldenrod";
}